package com.automatization.training.composition2.targil1;

public class Beard {
    private String colorBeard;
    private int lengthBeard;

    public Beard(String colorBeard, int lengthBeard){
        this.colorBeard = colorBeard;
        this.lengthBeard = lengthBeard;

    }

    public String getColorBeard() {
        return colorBeard;
    }

    public void setColorBeard(String colorBeard) {
        this.colorBeard = colorBeard;
    }

    public int getLengthBeard() {
        return lengthBeard;
    }

    public void setLengthBeard(int lengthBeard) {
        this.lengthBeard = lengthBeard;
    }

}
