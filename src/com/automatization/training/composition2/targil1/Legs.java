package com.automatization.training.composition2.targil1;

public class Legs {
    private int lenghtLegs;
    private String colorLegs;

    public Legs(int lenghtLegs, String colorLegs) {
        this.lenghtLegs = lenghtLegs;
        this.colorLegs = colorLegs;
    }

    public int getLenghtLegs() {
        return lenghtLegs;
    }

    public void setLenghtLegs(int lenghtLegs) {
        this.lenghtLegs = lenghtLegs;
    }

    public String getColorLegs() {
        return colorLegs;
    }

    public void setColorLegs(String colorLegs) {
        this.colorLegs = colorLegs;
    }
}
